function nav() {
    document.getElementById('navbar').style.display = 'block';
    document.getElementById('navbtn').style.display = 'none';
    document.getElementById('intro').style.marginTop = '40rem';
    document.getElementById('hide').style.display = 'block';
}
function navwrap() {
    document.getElementById('navbar').style.display = 'none';
    document.getElementById('navbtn').style.display = 'block';
    document.getElementById('intro').style.marginTop = '10rem';
    document.getElementById('hide').style.display = 'block';
}
